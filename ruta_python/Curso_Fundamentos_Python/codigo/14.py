#OPERADORES DE COMPARACION
#trabajar con numeros
#>: mayor
print(7>3) #True
print(3>7) #False
print(7>7) #False

#<: menor
print(5<6) #True
print(6<5) #False
print(6<6) #False

#>=: mayor o igual
print(2>=1) #True
print(2>=3) #False
print(2>=2) #True

#<=: menor o igual
print(1<=2) #True
print(2<=1) #False
print(2>=2) #True

#==: igualdad
print(6==6) #True
print(5==2) #False

#!=: diferente de
print(6!=10) #True
print(6!=6) #False

#trabajar con strings
print("Apple"=='Apple') #True
print("Apple"=='apple') #False

#trabajo con variables
age = 18
print(age>=18) #True
age = 15
print(age>=18) #False

